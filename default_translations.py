def get_default_translation(title: str, text: str, target_lang: str):
    lang_lower = target_lang.lower()
    if lang_lower not in TRANSLATIONS:
        return None
        
    lang_dict = TRANSLATIONS[lang_lower]
    # Match title keys ignoring whitespace and casing
    for key, val in lang_dict.items():
        if key.strip().lower() == title.strip().lower():
            return val
            
    return None
